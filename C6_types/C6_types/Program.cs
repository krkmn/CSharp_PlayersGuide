using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace C6_types
{
    class Program
    {
        static void Main(string[] args)
        {
            string first_string = "Hello, there";
            Console.WriteLine(first_string);

            bool first_bool = false;
            Console.WriteLine(first_bool);

            decimal first_decimal = 3.141592653589793M;
            Console.WriteLine(first_decimal);

            float first_float = 1.010f;
            Console.WriteLine(first_float);

            double first_double = 1.11111110;
            Console.WriteLine(first_double);

            byte first_byte = 200;
            Console.WriteLine(first_byte);

            sbyte first_sbyte = -100;
            Console.WriteLine(first_sbyte);

            int first_int = 1000000;
            Console.WriteLine(first_int);

            short first_short = 10000;
            Console.WriteLine(first_short);

            long first_long = 100000000;
            Console.WriteLine(first_long);

            uint first_uint = 1;
            Console.WriteLine(first_uint);

            ushort first_ushort = 10000;
            Console.WriteLine(first_ushort);

            ulong first_ulong = 123456112345;
            Console.WriteLine(first_ulong);

            char first_char = 'C';
            Console.WriteLine(first_char);

        }
    }
}
